## Since Matrix inversion is usually known as a computation that requires intense computing and 
## effort, it helps to automate the process. 

## The function "makeCacheMatrix" below creates an object of class "matrix" and caches its inverse.
makeCacheMatrix <- function(x = matrix()) {
        inv <- NULL
        set <- function(y) {
                x <<- y
                inv <<- NULL
        }
        get <- function() x
        setInverse <- function(inverse) inv <<- inverse
        getInverse <- function() inv
        list(set = set,
             get = get,
             setInverse = setInverse,
             getInverse = getInverse)
}


## This function takes the matrix created in "makeCacheMatrix" and computes its inverse or retrieves
## the inverse from the cache if already computed prior.

cacheSolve <- function(x, ...) {
        inv <- x$getInverse()
        if (!is.null(inv)) {
                message("getting cached data")
                return(inv)
        }
        mat <- x$get()
        inv <- solve(mat, ...)
        x$setInverse(inv)
        inv
}
